// MARK: - Label Picker View

private struct LabelPickerView: View {
    @Binding var selectedLabels: [CardLabel]
    let onDismiss: () -> Void

    @State private var searchText = ""
    @State private var customLabelName = ""
    @State private var customLabelColor = "#9E9E9E"

    private var availableLabels: [CardLabel] {
        CardLabel.presets.filter { preset in
            !selectedLabels.contains(where: { $0.id == preset.id })
        }
    }

    private var filteredLabels: [CardLabel] {
        if searchText.isEmpty {
            return availableLabels
        } else {
            return availableLabels.filter { $0.name.localizedCaseInsensitiveContains(searchText) }
        }
    }

    var body: some View {
        VStack(spacing: 0) {
            // Header
            HStack {
                Text("Add Labels")
                    .font(.system(size: 14, weight: .semibold, design: .monospaced))
                    .foregroundColor(.rtTextPrimary)

                Spacer()

                Button(action: onDismiss) {
                    Image(systemName: "xmark.circle.fill")
                        .font(.system(size: 18))
                        .foregroundColor(.rtMuted)
                }
                .buttonStyle(.plain)
            }
            .padding()
            .background(Color.rtBackgroundDark)
            .overlay(
                Rectangle()
                    .frame(height: 1)
                    .foregroundColor(.rtBorderSubtle),
                alignment: .bottom
            )

            // Search
            HStack {
                Image(systemName: "magnifyingglass")
                    .font(.system(size: 12))
                    .foregroundColor(.rtMuted)

                TextField("Search labels...", text: $searchText)
                    .textFieldStyle(.plain)
                    .font(.system(size: 12, design: .monospaced))
            }
            .padding(10)
            .background(Color.rtBackgroundDark.opacity(0.5))
            .cornerRadius(6)
            .padding()

            // Available labels
            ScrollView {
                VStack(alignment: .leading, spacing: 12) {
                    if !filteredLabels.isEmpty {
                        Text("Available Labels")
                            .font(.system(size: 10, weight: .semibold, design: .monospaced))
                            .foregroundColor(.rtTextSecondary)
                            .textCase(.uppercase)

                        FlowLayout(spacing: 8) {
                            ForEach(filteredLabels) { label in
                                Button(action: {
                                    selectedLabels.append(label)
                                }) {
                                    Text(label.name)
                                        .font(.system(size: 11, weight: .medium, design: .monospaced))
                                        .foregroundColor(Color(hex: label.color))
                                        .padding(.horizontal, 8)
                                        .padding(.vertical, 4)
                                        .background(Color(hex: label.color).opacity(0.15))
                                        .cornerRadius(4)
                                }
                                .buttonStyle(.plain)
                            }
                        }
                    } else {
                        Text("No labels found")
                            .font(.system(size: 11, design: .monospaced))
                            .foregroundColor(.rtMuted)
                    }
                }
                .padding()
            }
        }
        .frame(width: 400, height: 500)
        .background(Color.rtBackgroundLight)
    }
}

// MARK: - Flow Layout

private struct FlowLayout: Layout {
    var spacing: CGFloat = 8

    func sizeThatFits(proposal: ProposedViewSize, subviews: Subviews, cache: inout ()) -> CGSize {
        let result = FlowResult(
            in: proposal.replacingUnspecifiedDimensions().width,
            subviews: subviews,
            spacing: spacing
        )
        return result.size
    }

    func placeSubviews(in bounds: CGRect, proposal: ProposedViewSize, subviews: Subviews, cache: inout ()) {
        let result = FlowResult(
            in: bounds.width,
            subviews: subviews,
            spacing: spacing
        )
        for (index, subview) in subviews.enumerated() {
            subview.place(at: result.positions[index], proposal: .unspecified)
        }
    }

    struct FlowResult {
        var size: CGSize = .zero
        var positions: [CGPoint] = []

        init(in maxWidth: CGFloat, subviews: Subviews, spacing: CGFloat) {
            var x: CGFloat = 0
            var y: CGFloat = 0
            var lineHeight: CGFloat = 0

            for subview in subviews {
                let size = subview.sizeThatFits(.unspecified)

                if x + size.width > maxWidth && x > 0 {
                    x = 0
                    y += lineHeight + spacing
                    lineHeight = 0
                }

                positions.append(CGPoint(x: x, y: y))
                x += size.width + spacing
                lineHeight = max(lineHeight, size.height)
            }

            self.size = CGSize(width: maxWidth, height: y + lineHeight)
        }
    }
}

// MARK: - View Extensions

extension View {
    func onKeyboardShortcut(_ key: KeyEquivalent, action: @escaping () -> Void) -> some View {
        self.background(
            Button("") {
                action()
            }
            .keyboardShortcut(key, modifiers: [])
            .hidden()
        )
    }

    func onFocus(_ action: @escaping (Bool) -> Void) -> some View {
        self.onAppear {
            // Track focus state
        }
    }
}

